
function upperCase (str) {
    return str.toUpperCase();
}

function firstLetter (str) {
    return str[0];
}

function mergeTwoStr (str, str2){
    return str+str2;
}

console.log(upperCase('Renato Marques da Silva'))
console.log(firstLetter('renato'))
console.log(mergeTwoStr('Renato', 'Marques'))